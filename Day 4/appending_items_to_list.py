# Data Structure - LIST

states_of_america = ["Delaware", "Pennsylvania", "New Jersey", "Georgia", "Alaska", "Mexico", "Hawaii"]
print(states_of_america[0])

states_of_america[1] = "Pencilvania"  # This is how we can change the items in the list.
print(states_of_america)

states_of_america.append("Keval Panwala")  # This will add an item in the list at the end.
print(states_of_america)
states_of_america.extend(["Keval Land", "Malhar land", "Khushi land", "Dhvani land", "Jaivik land"])
print(states_of_america)

